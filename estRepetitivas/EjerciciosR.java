public class EjerciciosR {

    public static void imprimirWhileN1_10() {
        int numTnit=1;
        while (numTnit<=20) {
            System.out.println(numTnit);
            numTnit=numTnit+1;
        }
    }

    public static void main(String[] args) {
        imprimirWhileN1_10();
    }
}